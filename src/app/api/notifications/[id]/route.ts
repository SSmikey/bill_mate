      // ‡∏ñ‡πâ‡∏≤ mark as unread ‡πÉ‡∏´‡πâ‡∏•‡∏ö‡πÄ‡∏ß‡∏•‡∏≤
      if (!read) {
        notification.readAt = undefined;
      }
